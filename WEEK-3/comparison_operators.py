'''
>, >=, <, <=, ==, !=

After comparing operands we get boolean values: True or False
'''

print(4 > 3)
print(4 >= 3)
print(4 < 3)
print(4 <= 3)
print(3 == 4)
print(3 == '3')
print(str(3) == '3')
print(3 == int('3'))
print(3 == 3)

print(2 ** 10 == 1024)
print(2 * 5 == 10)

print(4 != '4')
print(3 != 4)

print(3 is not 4)
print(3 is 3)
print('land' in 'Finland')

